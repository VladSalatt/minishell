#include "libft.h"

int		ft_nbrlen(int n)
{
	int		len;

	if (n < -2147483647)
		return (1);
	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n = -n;
		len++;
	}
	while (n)
	{
		n /= 10;
		len++;
	}
	return (len);
}
